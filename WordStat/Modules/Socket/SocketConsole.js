$.SocketConsole = $.extend(
    
/*--------------------------------------------------------------------------------------------------
|
| -> Create
|
|-------------------------------------------------------------------------------------------------*/

    {public: {create: function(message) {
        console.log($.Colors.bgBlue(message));
    }}},
    
/*--------------------------------------------------------------------------------------------------
|
| -> In
|
|-------------------------------------------------------------------------------------------------*/

    {public: {'->': function(event, message) {
        console.log($.Colors.yellow($.Colors.bold('-> ' + event + ':') + ' ' + message));
    }}},
    
/*--------------------------------------------------------------------------------------------------
|
| -> Out
|
|-------------------------------------------------------------------------------------------------*/

    {public: {'<-': function(event, message) {
        console.log($.Colors.cyan($.Colors.bold('<- ' + event + ':') + ' ' + message));
    }}}
);

